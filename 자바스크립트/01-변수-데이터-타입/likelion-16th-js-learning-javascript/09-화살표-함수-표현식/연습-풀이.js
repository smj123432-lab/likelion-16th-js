// --------------------------------------------------------------------------
// 연습 문제: 웹사이트 URL 생성기 (기본 문법)
// --------------------------------------------------------------------------
// 도메인(domain)과 페이지 경로(path)를 받아 전체 URL 문자열을 만드는 함수입니다.
// 예: 'naver.com', 'news' -> 'https://naver.com/news'
//
// 조건:
// 1. 매개변수가 2개이므로 소괄호()를 꼭 작성
// 2. 중괄호{}와 return을 사용하는 "명시적 반환"

// [정답 및 해설]
// 매개변수가 2개 이상일 때는 소괄호 (a, b)가 필수입니다.
// 명시적 반환이므로 중괄호 { ... } 안에 return 키워드를 사용합니다.
const makeUrl = (domain, path) => {
  return 'https://' + domain + '/' + path
}

console.log(makeUrl('google.com', 'maps')) 
// 출력: https://google.com/maps


// --------------------------------------------------------------------------
// 연습 문제: 부가세(VAT) 계산기 (암묵적 반환)
// --------------------------------------------------------------------------
// 물건 가격(price)을 넣으면 부가세 10%(0.1)를 계산해주는 함수입니다.
//
// 조건:
// 1. 매개변수 소괄호() 생략 (매개변수가 1개)
// 2. 중괄호{}와 return 생략 (코드가 한 줄)

// [정답 및 해설]
// 매개변수가 하나(price)뿐이라 소괄호를 생략했습니다.
// 코드가 한 줄이라 중괄호와 return을 생략하고 바로 식을 적었습니다.
const calculateVat = price => price * 0.1

console.log(calculateVat(10000)) 
// 출력: 1000


// --------------------------------------------------------------------------
// 연습 문제: 버튼 컴포넌트 속성 만들기 (객체 반환)
// --------------------------------------------------------------------------
// 버튼의 레이블(label)과 컬러(color)를 입력받아, 설정 객체를 반환하는 함수입니다.
// 반환 형태: { label: '전송', color: 'blue', type: 'button' }
//
// 조건:
// 1. return 키워드를 생략하세요.
// 2. 객체를 반환할 때 소괄호()로 감싸는 것을 잊지 마세요.

// [정답 및 해설]
// 객체 리터럴 { ... }을 암묵적 반환으로 내보낼 때는
// 반드시 소괄호 ( ... )로 감싸야 함수 블록으로 오해받지 않습니다.
const createButtonProps = (label, color) => ({
  label: label,
  color: color,
  type: 'button'
})

console.log(createButtonProps('로그인', 'blue'))
// 출력: { label: '로그인', color: 'blue', type: 'button' }